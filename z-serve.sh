#!/bin/bash

###############################################################################
###############################################################################

USRNGRP="http:http"

###############################################################################
###############################################################################

case $1 in
    as)
        USRNGRP="${2}"
        EXECUSR="${USRNGRP/:*/}"
        EXECGRP="${USRNGRP/*:/}"
        sudo chown $EXECUSR:$EXECGRP . -R
        ;;
    *)
        EXECUSR="${USRNGRP/:*/}"
        EXECGRP="${USRNGRP/*:/}"
        sudo chown $EXECUSR:$EXECGRP . -R
        sudo -u $EXECUSR -g $EXECGRP ./serve.sh $1 $2 $3 $4 $5
esac

###############################################################################
###############################################################################

exit 0

