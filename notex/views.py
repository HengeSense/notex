from settings import SITE_HOST, SITE_NAME
from django.shortcuts import redirect

################################################################################
################################################################################

def page_not_found (request):

    if SITE_HOST != 'localhost':
        return redirect ('/' + SITE_NAME)
    else:
        return redirect ('/')

