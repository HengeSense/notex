import logging

################################################################################
################################################################################

class ExceptionLoggerMiddleware:

    def __init__ (self):

        self.logger = logging.getLogger (
            '%s.%s' % (__name__, self.__class__.__name__)
        )

    def process_exception (self, request, exception):

        self.logger.exception (exception)

